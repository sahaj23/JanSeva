    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        boolean isValidInput = validateInputs();
        if (isValidInput == false) {
            JOptionPane.showMessageDialog(null, "Username,Password,Email cannot be left blank", "ERROR!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            OperatorCredential solver = new OperatorCredential(UserName, new String(pwd), ConfEmail);
//            System.out.println((new String(pwd))+" "+ConfEmail+" "+selectedDept +" "+UserName);
            if (OperatorDAO.checkSolver(solver.getOperatorName())) {
                JOptionPane.showMessageDialog(null, "Username Already exists!!", "Error!", JOptionPane.ERROR_MESSAGE);
                return;
            } else if (AuthorityDAO.verifyConfidentialMail(ConfEmail)) {
                if (OperatorDAO.registerSolver(solver)) {
                    JOptionPane.showMessageDialog(null, "Registration Successful", "Success!", JOptionPane.INFORMATION_MESSAGE,icon2);
                    new LoginWindowAuthority().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Registration Rejected", "Error!", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(null, "Registration Rejected", "Error!", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "exception occoured", "EXCEPTION!", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

