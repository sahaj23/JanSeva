    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            String host = "redhat.com";
            int port = 80;
            int timeOutInMilliSec = 5000;// 5 Seconds
            Socket socket = new Socket();
            socket.connect(new InetSocketAddress(host, port), timeOutInMilliSec);
            System.out.println("Internet is Available");
        } catch (Exception ex) {
            System.out.println("No Connectivity");
            JOptionPane.showMessageDialog(null, "Oops no internet found!", "Error!", JOptionPane.WARNING_MESSAGE);
            return;
        }

        boolean isValidInput = validateInputs();
        if (isValidInput == false) {
            JOptionPane.showMessageDialog(null, "Username,Password cannot be left blank", "ERROR!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            OperatorCredential solver = new OperatorCredential(username, new String(pwd), null);
            if (OperatorDAO.verifySolver(solver)) {
                solver.setConfidentailEmail(AuthorityDAO.getAuthorityEmail(selectedOption));
//              JOptionPane.showMessageDialog(null,"Login Successful","Success!",JOptionPane.INFORMATION_MESSAGE);
                new AllMailAuthorityEnd(solver, AuthorityDAO.getSolverAuthority(solver)).setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Username,password incorrect", "Error", JOptionPane.ERROR_MESSAGE);
                jtxtPassword.setText("");
                jtxtUserName.setText("");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

