    private void btnNewCompActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewCompActionPerformed
        try {
            String host = "redhat.com";
            int port = 80;
            int timeOutInMilliSec = 5000;// 5 Seconds
            Socket socket = new Socket();
            socket.connect(new InetSocketAddress(host, port), timeOutInMilliSec);
            System.out.println("Internet is Available");
        } catch (Exception ex) {
            System.out.println("No Connectivity");
            JOptionPane.showMessageDialog(null, "Oops no internet found!", "Error!", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            UserEndComplaintWindow userEndWindow = new UserEndComplaintWindow(operator,AuthorityDAO.getOperatorAuthority(operator));
            userEndWindow.setVisible(true);
            this.dispose();
        }
        catch(SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error in DB", "Error",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnNewCompActionPerformed

