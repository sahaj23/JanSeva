    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        boolean isValidInput = validateInputs();
        if(isValidInput==false){
            JOptionPane.showMessageDialog(null,"Username,Password cannot be left blank","Oops!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            OperatorCredential user = new OperatorCredential();
            user.setOperatorName(username);
            user.setPassword(new String(pwd));
            boolean isValidUser = OperatorDAO.verifyUserEndOperator(user);
          
            if(isValidUser){
                JOptionPane.showMessageDialog(null,"Make Sure you are connected to the internet!","Note!",JOptionPane.WARNING_MESSAGE);
                new SelectOptionUserEnd(user).setVisible(true);
//                this.dispose();
          
        }
          else
            {
                JOptionPane.showMessageDialog(null,"Username,password incorrect","Error",JOptionPane.ERROR_MESSAGE);
                jtxtPasswordLog.setText("");
                jtxtUserNameLog.setText("");
            }
        }
        catch(Exception e){
        e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
         boolean isValidInput = validateInputsReg();
        if(isValidInput==false){
            JOptionPane.showMessageDialog(null,"Username,Password,email cannot be left blank","Oops!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            OperatorCredential user = new OperatorCredential(usernameReg,new String(pwdReg),ConfEmailReg);
            boolean registerOperator = OperatorDAO.registerUserEndOperator(user);
            if(registerOperator){
                JOptionPane.showMessageDialog(null,"Registration Successful","Success!",JOptionPane.INFORMATION_MESSAGE,icon2);
            }
            else{
                 JOptionPane.showMessageDialog(null,"Registration Rejected","Error!",JOptionPane.ERROR_MESSAGE);
                 return;
            }
        }
        catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"Exception occoured","EXCEPTION!",JOptionPane.ERROR_MESSAGE);
            return;
       }
    }//GEN-LAST:event_btnRegisterActionPerformed

