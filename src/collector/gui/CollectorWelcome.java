    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            String host = "redhat.com";
            int port = 80;
            int timeOutInMilliSec = 5000;// 5 Seconds
            Socket socket = new Socket();
            socket.connect(new InetSocketAddress(host, port), timeOutInMilliSec);
            System.out.println("Internet is Available");
        } catch (Exception ex) {
            System.out.println("No Connectivity");
            JOptionPane.showMessageDialog(null, "Oops no internet found!", "Error!", JOptionPane.WARNING_MESSAGE);
            return;
        }

        boolean isValidInput = validateInputs();
        if (isValidInput == false) {
            JOptionPane.showMessageDialog(null, "Password cannot be left blank", "ERROR!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            boolean isValidUser = CollectorDAO.verifyCollector(new String(pwd));
            if (isValidUser) {
                JOptionPane.showMessageDialog(null, "Make Sure you are connected to the internet!", "Note!", JOptionPane.WARNING_MESSAGE);
                this.dispose();

                new AllMailCollectorEnd(CollectorDAO.getConfidentialMail()).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "password incorrect", "Error", JOptionPane.ERROR_MESSAGE);
                jtxtPassword.setText("");

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnLoginActionPerformed

