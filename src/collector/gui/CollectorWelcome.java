    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        boolean isValidInput = validateInputsReg();
        if (isValidInput == false) {
            JOptionPane.showMessageDialog(null, "Password,Email cannot be left blank", "ERROR!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            if (CollectorDAO.checkCollector(new CollectorCredential(null, jtxtMail.getText()))) {
                JOptionPane.showMessageDialog(null, "Collector Account Already Exists!!", "Information", JOptionPane.INFORMATION_MESSAGE);
                jtxtPasswordReg.setText("");
                jtxtMail.setText("");
                return;
            } else {
                CollectorCredential collector = new CollectorCredential(new String(pwdReg), ConfEmail);
                if (CollectorDAO.registerCollector(collector)) {
                    JOptionPane.showMessageDialog(null, "Registration Successful", "Success!", JOptionPane.INFORMATION_MESSAGE,icon2);
                } else {
                    JOptionPane.showMessageDialog(null, "Registration Rejected", "Error!", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "exception occoured", "EXCEPTION!", JOptionPane.ERROR_MESSAGE);
            return;
        }

    }//GEN-LAST:event_btnRegisterActionPerformed

