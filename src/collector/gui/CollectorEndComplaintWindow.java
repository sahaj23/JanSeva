    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        String selectedDept  = jcbMailTo.getSelectedItem().toString();
        String mail = jtxtName.getText()+"~"+jtxtAadhar.getText()+"~"+jtxtSubject.getText()+"~"+jtxtAreaMatter.getText()+"~"+jtxtAdd.getText()+"~"+jtxtMail.getText()+"~"+jtxtPhn.getText()+"~"+jtxtAreaNote.getText();
        
        Properties props = new Properties();
        props.put("mail.smtp.host", "smtp.gmail.com");
        props.put("mail.smtp.socketFactory.port", "465");
        props.put("mail.smtp.socketFactory.class","javax.net.ssl.SSLSocketFactory");
        props.put("mail.smtp.auth", "true");
        props.put("mail.smtp.port", "465");
        //get Session   
        Session session = Session.getInstance(props,
                new javax.mail.Authenticator() {
                protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(collector.getConfidentialEmail(), collector.getPassword());
            }
        });
        try {
            MimeMessage msg = new MimeMessage(session);
            msg.addRecipient(Message.RecipientType.TO, new InternetAddress(AuthorityDAO.getAuthorityEmail(selectedDept)));
            msg.setSubject(message.getSubject());
            MimeBodyPart bodyPart = new MimeBodyPart();
            bodyPart.setText(mail);
            Multipart multipart = new MimeMultipart();
            MimeBodyPart attachment = new MimeBodyPart();
            attachment.attachFile(location);
            multipart.addBodyPart(attachment);
            multipart.addBodyPart(bodyPart);
            msg.setContent(multipart);
            Transport.send(msg);
            System.out.println("message sent successfully");
                JOptionPane.showMessageDialog(null,"Message Sent Successfully!","Success!",JOptionPane.INFORMATION_MESSAGE);
    } catch (MessagingException e) {
            throw new RuntimeException(e);
        } catch (IOException ex) {
            Logger.getLogger(Mailer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CollectorEndComplaintWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSendActionPerformed

